package main

import "fmt"

func main() {
	// Declare variables
	var x int = 4
	var y int = 7

	// Perform calculations
	sum := x + y
	diff := x - y
	prod := x * y
	quot := x / y

	// Print results
	fmt.Println("Sum:", sum)
	fmt.Println("Difference:", diff)
	fmt.Println("Product:", prod)
	fmt.Println("Quotient:", quot)
}